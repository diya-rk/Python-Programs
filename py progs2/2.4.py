#capitalizing the elements of a list
n=["apple","mango","cherry","orange"]
print(n)
new = []
for i in n:
    new.append(i.upper())
print(new)
#capitalizing the first letter of elements in a list
n=["apple","mango","cherry","orange"]
print(n)
new = []
for i in n:
    new.append(i.capitalize())
print(new)

